package com.example.mvp.ui.util


/**
 * Enumeration class representing the display options for the tab row.
 *
 * The options are [Scrollable] and [Expanded].
 */
enum class TabRowType {
   Scrollable,Expanded
}

/**
 * Enumeration class representing the size options for the grid used to display restaurant tiles.
 *
 * The options are [Fixed] and [Adaptive].
 */
enum class GridSize {
   Fixed,Adaptive
}